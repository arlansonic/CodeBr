/* 01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores.
*/

// Soma 
function soma(x, y){
    return x + y
}
console.log('Valor da Soma:' + soma(1,2))

// Subtração

function sub(x, y){
    return x - y
}
console.log('Resultado Subtração: '+sub(2,2))

// Multiplicação
function mult(x,y){
    return x * y
}
console.log('Resultado Multiplicação: ' +mult(5,5))

// Divisão

function divisao(x, y){
    return x / y
}

console.log('Resultado da Divsião: '+ divisao(25,2))

